/* eslint-disable no-use-before-define */
import React from 'react';
import TextField from '@material-ui/core/TextField';
import Autocomplete from '@material-ui/lab/Autocomplete';
import Button from '@material-ui/core/Button';
import Dialog from '@material-ui/core/Dialog';
import DialogActions from '@material-ui/core/DialogActions';
import DialogContent from '@material-ui/core/DialogContent';
import DialogContentText from '@material-ui/core/DialogContentText';
import DialogTitle from '@material-ui/core/DialogTitle';

export default function NoOptions() {
  const [top100Films, setTop100] = React.useState([...top100Films2]);

  const [newFilm, setNewFilm] = React.useState({ title: '', year: null });

  const [selected, setSelected] = React.useState(top100Films2[0]);

  const [open, setOpen] = React.useState(false);

  const handleOpen = () => {
    setOpen(true);
  };

  const handleCancel = () => {
    setOpen(false);
    setNewFilm({ title: '', year: null });
  };

  const handleAdd = () => {
    setTop100([...top100Films, newFilm]);
    setSelected(newFilm);
    setNewFilm({ title: '', year: null });
    setOpen(false);
  };

  return (
    <>
      <Dialog open={open} onClose={handleCancel} aria-labelledby="form-dialog-title">
        <DialogTitle id="form-dialog-title">Add a new film</DialogTitle>
        <DialogContent>
          <DialogContentText>
            If you think we missed some film, please enter the title and year here.
          </DialogContentText>
          <TextField
            onChange={event =>
              setNewFilm({
                ...newFilm,
                title: event.target.value,
              })
            }
            value={newFilm.title}
            autoFocus
            placeholder="The Shawshank Redemption"
            margin="dense"
            id="title"
            label="Title"
            type="text"
            fullWidth
          />
          <TextField
            onChange={event =>
              setNewFilm({
                ...newFilm,
                year: event.target.value,
              })
            }
            value={newFilm.year}
            margin="dense"
            placeholder="1994"
            id="year"
            label="Year"
            type="number"
            fullWidth
          />
        </DialogContent>
        <DialogActions>
          <Button onClick={handleCancel} color="primary">
            Cancel
          </Button>
          <Button onClick={handleAdd} color="primary">
            Add
          </Button>
        </DialogActions>
      </Dialog>
      <Autocomplete
        id="no-options-demo"
        value={selected}
        style={{ width: 300 }}
        options={top100Films}
        getOptionLabel={option => option.title}
        renderInput={params => (
          <TextField {...params} label="no-options" variant="outlined" fullWidth margin="normal" />
        )}
        onInputChange={(_, value) => {
          console.log(value);
          // setNewFilm({...newFilm, title: value})
        }}
        noOptionsText={`Create option ${newFilm.title}`}
        onNoOptionsSelected={handleOpen}
      />
    </>
  );
}

